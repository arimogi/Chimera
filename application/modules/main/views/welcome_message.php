<h3>Overview</h3>

<p>Chimera is a combination of CodeIgniter and some useful libraries, javascript frameworks etc. These are the building blocks of chimera :</p>
<ul>
  <li>CodeIgniter 2.0.3 [<a href="<?php echo base_url(); ?>user_guide/" target="_blank">User Guide</a>]</li>
  <li>HMVC [<a href="https://bitbucket.org/wiredesignz/codeigniter-modular-extensions-hmvc/wiki/Home" target="_blank">User Guide (Online)</a>]</li>
  <li>ion_auth 2.0.4 [<a href="http://getsparks.org/packages/ion_auth/versions/HEAD/show" target="_blank">Spark Version User Guide (Online)</a>] [<a href="http://benedmunds.com/ion_auth/" target="_blank">User Guide (Online)</a>]</li>
  <li>doctrine2 [<a href="http://getsparks.org/packages/doctrine2/versions/HEAD/show" target="_blank">Spark Version User Guide (Online)</a>] [<a href="http://www.doctrine-project.org/docs/orm/2.0/en/" target="_blank">User Guide (Online)</a>]</li> 
  <li>asset_loader 1.0.0 alpha</li>
  <li>Phil Sturgeon's template library</li>
  <li>Spark 0.0.5 [<a href="http://getsparks.org/" target="_blank">User Guide (Online)</a>]</li>                  
  <li>JQuery 1.6 [<a href="http://docs.jquery.com/Main_Page/" target="_blank">User Guide (Online)</a>]</li>
  <li>Extjs 4.0.0 [<a href="http://www.sencha.com/products/extjs/" target="_blank">User Guide (Online)</a>]</li></li>
  <li>Persistence Grid 1.0.0 alpha</li>
</ul>

